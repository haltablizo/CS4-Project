
package arachne;


public class Spider {
    private int atk; 
    private int def; 
    private int hp;
    private int maxHp; 
    private int amtOfSilk; 
    
    public Spider(int a, int d, int h, int m, int amt) {
        this.atk = a; 
        this.def = d; 
        this.hp = h; 
        this.maxHp = m; 
        this.amtOfSilk = amt; 
    }
    
    public int getAtk() {
        return this.atk; 
    }     
    public int getDef() {
        return this.atk; 
    }     
    public int getHp() {
        return this.atk; 
    }    
    public int getMaxHp() {
        return this.atk; 
    }
    public int getAmtOfSilk() {
        return this.amtOfSilk; 
    }
}
